    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        // TODO add your handling code here:
        int opcion = this.jComboBox1.getSelectedIndex();
        
        switch(opcion){
            
        case 0:
             break;
            
        case 1:
            this.jButton1.setVisible(true);
            this.jButton1.setText("PRESTAR");
            this.dniLabel.setVisible(true);
            this.TextField.setVisible(true);
            this.jLabel1.setVisible(false);
            this.jTextArea1.setVisible(false);
                break;
        case 2:
        
            this.jButton1.setVisible(true);
            this.jButton1.setText("DEVOLVER");
            this.dniLabel.setVisible(true);
            this.TextField.setVisible(true);
            this.jLabel1.setVisible(false);
            this.jTextArea1.setVisible(false);
            break;
        default:
            this.jButton1.setVisible(true);
            this.jButton1.setText("CONSULTAR");
            this.dniLabel.setVisible(true);
            this.TextField.setVisible(true);
            this.jLabel1.setVisible(true);
            this.jTextArea1.setVisible(true);
            break;
        }
    }//GEN-LAST:event_jComboBox1ActionPerformed

