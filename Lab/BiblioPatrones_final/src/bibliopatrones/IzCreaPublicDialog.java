    private void seleccionPublijComboBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_seleccionPublijComboBoxActionPerformed
        int seleccion = seleccionPublijComboBox.getSelectedIndex();
        switch (seleccion){
            case 0:
                //Cambiar layout a libro
                System.out.println("case 0 de comboBox");                
                this.param1Label.setText("Editorial");
                this.param2Label.setText("Contenido");
                this.param3Label.setText("Edición");
                break;
            case 1:
                //Cambiar a revista
                System.out.println("caso1 del comboBox");
                this.param1Label.setText("Periodicidad");
                this.param2Label.setText("Volumen");
                this.param3Label.setText("NºPublicación");
                
                break;
            default:
                //Cambiar a tesis
                System.out.println("caso default del comboBox");
                this.param1Label.setText("Tribunal");
                this.param2Label.setText("Departamento");
                this.param3Label.setText("Calificación");
                
                break;
       }
            
    }//GEN-LAST:event_seleccionPublijComboBoxActionPerformed

