    /**
     Abre una ventana para seleccionar una foto
     */
    private void btnFotoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFotoActionPerformed
        JFileChooser fileChooser = new JFileChooser();
        int seleccion = fileChooser.showSaveDialog(this);
        if (seleccion == JFileChooser.APPROVE_OPTION)
        {
           File fichero = fileChooser.getSelectedFile();
           if(!fichero.getName().contains(".png")){
               JOptionPane.showMessageDialog(this, "Introduce un archivo correcto", "Error de Archivo", JOptionPane.INFORMATION_MESSAGE);
           }
           else{
            txtFoto.setText(fichero.getName());
           }
        }
    }//GEN-LAST:event_btnFotoActionPerformed
    /**
     Rellena los combobox cno y oficinas
     */
    public void rellenarCNO(){
            ArrayList al = UtilAgencia.getOcupacionCNO();
            for (int i = 0; i < al.size(); i++) {
                cbOcu.addItem(al.get(i));
            }
            ArrayList<Oficina> o = UtilAgencia.getOficinas();
                for (int i = 0; i < o.size(); i++) {
                    cbOficina.addItem(o.get(i).getNombre());
                }
            if(!o.isEmpty()) {
                cbOficina.setSelectedIndex(0);
            }
        
    }
    
