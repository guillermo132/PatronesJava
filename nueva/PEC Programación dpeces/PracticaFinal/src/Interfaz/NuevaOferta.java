    /**
     Agrega la Oferta con los datos que hemos recibido
     */
    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        
        try{
            int rango = Integer.parseInt(txtRango.getText().substring(0,5));
            String cnoo = cbOcu.getSelectedItem().toString();
            String oficina = cbOficina.getSelectedItem().toString().substring(0, 4);
            String empresa = txtNombre.getText().substring(0,4);
                    if(oficina.equals(empresa)){
                    
                
                        if( UtilAgencia.comprobarListaCNOOferta(cnoo)) {
                            Oficina ofi = UtilAgencia.getOficina(cbOficina.getSelectedItem().toString());
                            String dirFoto = "img/"+txtFoto.getText();
                            Oferta of;
                            String cno = cbOcu.getSelectedItem().toString().substring(0,4);
                            //Sacar las fechas
                            String [] fo = txtFofe.getText().split("/");
                            String [] fc = txtFcad.getText().split("/");
                            int mesO = Integer.parseInt(fo[1]); mesO--;
                            int mesC = Integer.parseInt(fc[1]); mesC--;
                            GregorianCalendar fofe = new GregorianCalendar(Integer.parseInt(fo[2]), mesO, Integer.parseInt(fo[0]));
                            GregorianCalendar fcad = new GregorianCalendar(Integer.parseInt(fc[2]), mesC, Integer.parseInt(fc[0]));
                            String dispo = txtDispo.getText();
                            if(rbParcial.isSelected() && Integer.parseInt(dispo) > 100) dispo = "100";
                            if(rbCompleto.isSelected()){
                                of = new Oferta(txtNombre.getText(), ofi, cno, txtPalabras.getText(), fofe, fcad, txtRango.getText(),"Completo",dirFoto, 0);
                            }else{
                                of = new Oferta(txtNombre.getText(), ofi, cno, txtPalabras.getText(), fofe, fcad, txtRango.getText(), "Parcial",dirFoto, Integer.parseInt(dispo));
                            }
                            UtilAgencia.nuevaOferta(of);
                            JOptionPane.showMessageDialog(this, "Oferta de "+txtNombre.getText()+" agregada", "Oferta Agregada", JOptionPane.INFORMATION_MESSAGE);
                            limpiarCampos();
                        }else{
                            JOptionPane.showMessageDialog(this, "Introduzca bien el Rango Salarial.", "Erorr", JOptionPane.ERROR_MESSAGE);
                        }
                    }else
                    {
                        JOptionPane.showMessageDialog(this, "Introduzca bien oficina.", "Erorr", JOptionPane.ERROR_MESSAGE);
                    }
           
        }catch(NullPointerException e){JOptionPane.showMessageDialog(this, "Introduzca bien los datos.", "Erorr", JOptionPane.ERROR_MESSAGE);}
        catch(NumberFormatException e){JOptionPane.showMessageDialog(this, "Introduzca bien los datos.", "Error", JOptionPane.ERROR_MESSAGE);}
        catch(Exception e){JOptionPane.showMessageDialog(this, "Error: Introduzca todos los datos \n"+e.toString(), "Error", JOptionPane.INFORMATION_MESSAGE);}
    
    }//GEN-LAST:event_btnAgregarActionPerformed

