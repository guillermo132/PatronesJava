    /**
     Seleccionar Candidato y ver sus datos (Foto, cno y palabras clave)
     */
    private void tablaCandidatosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaCandidatosMouseClicked
        btnBorrar.setEnabled(true);
        int row = tablaCandidatos.getSelectedRow();
        String nif = tablaCandidatos.getValueAt(row, 0).toString();
        String cno = tablaCandidatos.getValueAt(row, 8).toString();
        txtPalabras.setText(tablaCandidatos.getValueAt(row, 9).toString());
        labelFoto.setIcon(UtilAgencia.mostrarFotoCandidato(nif));
        modelo.clear();
        ArrayList acno = UtilAgencia.getNombresCNO(cno);
        for (int i = 0; i < acno.size(); i++) {
            modelo.addElement(acno.get(i).toString());
        }
        
    }//GEN-LAST:event_tablaCandidatosMouseClicked

    /**
     Borra un candidato
     */
    private void btnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActionPerformed
        int row = tablaCandidatos.getSelectedRow();
        String nombre = tablaCandidatos.getValueAt(row, 1).toString();
        int respuesta = JOptionPane.showConfirmDialog(this, "¬øSeguro que desea borrar al candidat@: "+nombre+"?", "Borrar Candidat@", JOptionPane.YES_NO_OPTION);
        
        if(respuesta == JOptionPane.YES_OPTION){
            UtilAgencia.borrarCandidatoVisor(row);
            JOptionPane.showMessageDialog(this, "Candidat@ "+nombre+" borrad@.", "Candidat@ Borrad@", JOptionPane.INFORMATION_MESSAGE);
            UtilAgencia.mostrarCandidatos(tablaCandidatos);
            modelo.clear();
            btnBorrar.setEnabled(false);
            labelFoto.setIcon(null);
        }
    }//GEN-LAST:event_btnBorrarActionPerformed

    /**
     Moverse por la tabla con las teclas
     */
    private void tablaCandidatosKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tablaCandidatosKeyReleased
       if(evt.getKeyCode() == 38) tablaCandidatosMouseClicked(null); //Flecha hacia arriba
        if(evt.getKeyCode() == 40) tablaCandidatosMouseClicked(null); //Flecha hacia arriba
    }//GEN-LAST:event_tablaCandidatosKeyReleased

    
