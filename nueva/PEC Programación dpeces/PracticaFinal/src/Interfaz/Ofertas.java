    /**
     selecciona una oferta para visualizarla
     */
    private void tablaOfertasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaOfertasMouseClicked
        btnBorrar.setEnabled(true);
        int row = tablaOfertas.getSelectedRow();
        String cno = tablaOfertas.getValueAt(row, 2).toString();
        String ofertas = tablaOfertas.getValueAt(row, 3).toString();
        String nombreE = tablaOfertas.getValueAt(row, 0).toString();
        labelFoto.setIcon(UtilAgencia.mostrarFotoEmpresa(nombreE));
        ArrayList al = UtilAgencia.getNombresCNO(cno);
        txtCNO.setText(al.get(0).toString());
        txtPalabrasC.setText(ofertas);
    }//GEN-LAST:event_tablaOfertasMouseClicked

    /**
     Movernos por la tabla con las teclas
     */
    private void tablaOfertasKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tablaOfertasKeyReleased
        if(evt.getKeyCode() == 38) tablaOfertasMouseClicked(null); //Flecha hacia arriba
        if(evt.getKeyCode() == 40) tablaOfertasMouseClicked(null); //Flecha hacia abajo
    }//GEN-LAST:event_tablaOfertasKeyReleased
        
    /**
     Borrar una oferta
     */    
    private void btnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActionPerformed
        
        int row = tablaOfertas.getSelectedRow();
        String oficina = tablaOfertas.getValueAt(row, 1).toString();
        int respuesta = JOptionPane.showConfirmDialog(this, "¿Seguro que desea borrar la oferta de: "+oficina+"?", "Borrar Oferta", JOptionPane.YES_NO_OPTION);

        if(respuesta == JOptionPane.YES_OPTION){
            UtilAgencia.borrarOfertaVisor(row);
            JOptionPane.showMessageDialog(this, "Oferta de "+oficina+" borrada.", "Oferta Borrada", JOptionPane.INFORMATION_MESSAGE);
            UtilAgencia.mostrarOfertas(tablaOfertas);
            txtCNO.setText("");
            txtPalabrasC.setText("");
            btnBorrar.setEnabled(false);
            labelFoto.setIcon(null);
        }
        
    }//GEN-LAST:event_btnBorrarActionPerformed

    
