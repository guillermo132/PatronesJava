    private void jButtonCanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCanActionPerformed
        String[] opciones = {"CNO", "Claves"};

        int n;
        n = JOptionPane.showOptionDialog(this,
                "Tipo de Búsqueda",
                "¿Cómo realizar la búsqueda?",
                JOptionPane.YES_NO_CANCEL_OPTION,
                JOptionPane.QUESTION_MESSAGE,
                null,
                opciones,
                opciones[0]);
        if (n == JOptionPane.YES_OPTION) {
            OfertaImp imp = new OfertaImpCno();
            abs.setImp(imp);
             if (abs.buscar(of)) {
                    JOptionPane.showMessageDialog(this, "Se han encontrado : " + UtilSistema.getCandiCasan().size() + " coincidencias. "+of.getOficina().getNomCompañia(), "EXITO", JOptionPane.INFORMATION_MESSAGE);
                    CanParaOf a = new CanParaOf(this);
                } else {//Si es falso se no se han encontrado coincidencias
                    JOptionPane.showMessageDialog(this, "No se han encontrado coincidencias", "Informacion", JOptionPane.INFORMATION_MESSAGE);
                }
        } else if (n == JOptionPane.NO_OPTION) {
            OfertaImp imp = new OfertaImpClave();
            abs.setImp(imp);
            if (abs.buscar(of)) {
                    JOptionPane.showMessageDialog(this, "Se han encontrado: " + UtilSistema.getCandiCasan().size()+ " coincidencias.", "EXITO", JOptionPane.INFORMATION_MESSAGE);
                    CanParaOf b = new CanParaOf(this);
                } else {//si es falso no se han encontrado coincidencias
                    JOptionPane.showMessageDialog(this, "No se han encontrado coincidencias", "Informacion", JOptionPane.INFORMATION_MESSAGE);
                }
        } else if (n == JOptionPane.CANCEL_OPTION) {
            this.setVisible(true);
        }
    }//GEN-LAST:event_jButtonCanActionPerformed
    /**
     * Permite visualizar los datos de las ofertas
     */
    private void verDatos(Oferta of) {

        String tipo;
        String claves = "";
        for (int i = 0; i < of.getClaves().length; i++) {
            claves += of.getClaves()[i] + ",";//Obtiene las claves

        }
         //PARA ADAPTER
        s = new AdaptadorSueldo(of);
        
        jTextFieldNomCompañia.setText(of.getOficina().getNomCompañia());
        jTextFieldNomOficina.setText(of.getOficina().getNomOficina());
        jTextFieldTlf.setText(Integer.toString(of.getOficina().getTlf()));
        jTextFieldDireccion.setText(of.getOficina().getDireccion().getNombre());
        jTextFieldNum.setText(Integer.toString(of.getOficina().getDireccion().getNumero()));
        jTextFieldPiso.setText(Integer.toString(of.getOficina().getDireccion().getPiso()));
        jTextFieldLetra.setText(of.getOficina().getDireccion().getLetra());
        jTextFieldLoca.setText(of.getOficina().getDireccion().getLocalidad());
        jTextFieldCodPos.setText(Integer.toString(of.getOficina().getDireccion().getCodigoPostal()));
        jTextFieldOcupaciones.setText(of.getOcupacion().toString());
        jTextFieldClaves.setText(claves);
        jTextFieldSalMin.setText(Double.toString(of.getSalMin()));
        jTextFieldSalMax.setText(Double.toString(of.getSalMax()));
        //PARA ADAPTER
        jTextFieldSueldoNeto.setText(s.getSueldoNeto());
        if (of.getTipo().equals("Parcial")) {
            jLabelDedicacion.setVisible(true);
            jTextFieldDedicacion.setVisible(true);
            jTextFieldDedicacion.setText(Double.toString(of.getGrado()));
        } else {
            jLabelDedicacion.setVisible(false);
            jTextFieldDedicacion.setVisible(false);
        }
        
       of.cambiar(this);
        
        
    }
