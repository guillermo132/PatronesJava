    private void verOfertas() {
        try {
            ofAux = UtilSistema.getOfertas();//Array igual que el array de ofertas
            ite = ofAux.listIterator();//Iterator para recorrer las ofertas

            if (ofAux.size() < 1) {//Si el tamaño es menor que uno lanza un error
                JOptionPane.showMessageDialog(this, "Error, no hay ofertas", "ERROR", JOptionPane.ERROR_MESSAGE);
                jButtonAnt.setEnabled(false);
                jButtonSig.setEnabled(false);
            } else {
                jButtonAnt.setEnabled(true);
                jButtonSig.setEnabled(true);
            }

            if (iterador.hayMas()) {//Si hay ofertas las convierte a objeto 
                of = (Oferta) iterador.siguiente();

            }
            if (of != null) {//Si no es nula la presenta
                verDatos(of);
            } else {//No hay mas ofertas
                JOptionPane.showMessageDialog(this, "No hay mas ofertas", "ERROR", JOptionPane.ERROR_MESSAGE);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "ERROR: " + e.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }
