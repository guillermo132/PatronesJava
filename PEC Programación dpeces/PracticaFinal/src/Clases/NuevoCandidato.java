    /**
     Agrega el nuevo candidato
     */
    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        try{
            String cno = UtilAgencia.getListaCBCNO();
            String dirFoto = "img/"+txtFoto.getText();
            int dis;
            if(!cbDis.isSelected()) {dis=0;}else{dis=Integer.parseInt(txtDis.getText());}
            Direccion dir = new Direccion(pd.getTxtCalle(), Integer.parseInt(pd.getTxtNum()), pd.getTxtPyL(), pd.getTxtLocalidad(), pd.getTxtCodPos());
            Candidato can = new Candidato(txtNif.getText(), txtNombre.getText(), txtTelefono.getText(), dir, cno, dirFoto, txtPalabras.getText(), txtRango.getText(), dis);
            UtilAgencia.nuevoCandidato(can);
            JOptionPane.showMessageDialog(this, "Candidat@ "+txtNombre.getText()+" agregad@", "Candidat@ Recibid@", JOptionPane.INFORMATION_MESSAGE);
            UtilAgencia.borrarListaCBCNO();
            limpiarCampos();
        }catch(NullPointerException e){JOptionPane.showMessageDialog(this, "Introduzca bien los datos.", "Erorr", JOptionPane.ERROR_MESSAGE);}
        catch(NumberFormatException e){JOptionPane.showMessageDialog(this, "Introduzca bien los datos.", "Error", JOptionPane.ERROR_MESSAGE);}
        catch(Exception e){JOptionPane.showMessageDialog(this, "Error: "+e.toString(), "Error", JOptionPane.INFORMATION_MESSAGE);}
    
    }//GEN-LAST:event_btnAgregarActionPerformed

    /**
     Lanza una ventana para seleccionar una foto
     */
    private void btnFotoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFotoActionPerformed
        JFileChooser fileChooser = new JFileChooser();
        int seleccion = fileChooser.showSaveDialog(this);
        if (seleccion == JFileChooser.APPROVE_OPTION)
        {
           File fichero = fileChooser.getSelectedFile();
           if(!fichero.getName().contains(".png")){
               JOptionPane.showMessageDialog(this, "Introduce un archivo correcto", "Error de Archivo", JOptionPane.INFORMATION_MESSAGE);
           }
           else{
           txtFoto.setText(fichero.getName());
           }
        }
    }//GEN-LAST:event_btnFotoActionPerformed

