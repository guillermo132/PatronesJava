    private void rellenarCNO(){
            ArrayList al = UtilAgencia.getOcupacionCNO();
            for (int i = 0; i < al.size(); i++) {
                cbCNO.addItem(al.get(i));
            }
    }
    
    private void limpiarCampos(){
       txtNif.setText(""); 
       txtNombre.setText("");  
       txtTelefono.setText(""); 
       txtPalabras.setText(""); 
       txtFoto.setText(""); 
       txtDis.setText(""); 
       txtRango.setText("");  
       pd.setTxtCalle("");
       pd.setTxtCodPos("");
       pd.setTxtNum("");
       pd.setTxtPyL("");
       pd.setTxtLocalidad("");
       cbDis.setSelected(false);
       cbCNO.setSelectedIndex(0);
       modelo.clear();
       
       
    }
   
