    /**
     Comprueba si ya ha metido ese mismo CNO, y lo agrega
     */
    private void btnAgregarCNOActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarCNOActionPerformed
        String cno = cbCNO.getSelectedItem().toString();
        Boolean valido = UtilAgencia.comprobarListaCNO(cno);
        if(valido){    
            modelo.addElement(cno);
        }else
        {
            JOptionPane.showMessageDialog(this, "Eliga una Ocupación Correcta, y no vale repetir", "Inténtalo de Nuevo", JOptionPane.INFORMATION_MESSAGE);
        } 
    }//GEN-LAST:event_btnAgregarCNOActionPerformed

